IconElementCanvasUtil.createBuildingElementDeviceIcon = function(elementType, elementTypeGroup, color) {
	switch (elementType) {
	case "auditorium":
		return IconBuildingCanvasUtil.roomAuditorium(color);
	case "class":
		return IconBuildingCanvasUtil.roomClass(color);
	case "cafeteria":
		return IconBuildingCanvasUtil.roomCafeteria(color);
	case "elevator":
		return IconBuildingCanvasUtil.roomElevator(color);
	case "stairs":
		return IconBuildingCanvasUtil.roomStairs(color);
	case "wc":
		return IconBuildingCanvasUtil.roomWc(color);
	}
	return null;
};

